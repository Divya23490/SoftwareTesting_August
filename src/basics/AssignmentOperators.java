package basics;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a=4; 
		int var;//0
		System.out.println(var=a);
		var+=a;//4+4
		System.out.println(var);
		var*=a; //8*4
		System.out.println(var);
		var/=a; //32/8
		System.out.println(var);
		var%=a;
		System.out.println(var);
		

	}

}
