package basics;

public class RelationalOperators {

	public static void main(String[] args) {
		System.out.println(121<100);
		System.out.println(121>100);
		System.out.println(121>=100);
		System.out.println(121<=100);
		System.out.println(121!=100);
		System.out.println(121==100);

	}

}
